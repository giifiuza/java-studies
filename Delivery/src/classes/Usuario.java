package classes;

public class Usuario {

    private int id;
    private String user;

    private String senha;

    public int getId() {
        return id;
    }
    public String getUser() {
        return user;
    }

    public String getSenha() {
        return senha;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }


}
